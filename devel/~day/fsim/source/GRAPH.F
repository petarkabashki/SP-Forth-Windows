\ вывод графика

\ 1 Рассчитать Х
\ 2 Рассчитать Y
\ 3 Вывести точку


0 VALUE Xmin \ координаты экрана - min и max
             \ - окно в окне Windows
0 VALUE Xmax \ заполняются при запуске программы

.E FVALUE НачВремяГрафика 
.E FVALUE КонВремяГрафика \ уст пользователем
                       

: РасчетX ( F: r1 -- r2 )
      НачВремяГрафика F-  
      Xmax Xmin - DS>F F*
      КонВремяГрафика НачВремяГрафика F- 
      F/                     
      Xmin DS>F F+   
;

0 VALUE Ymax  \ заполняются при запуске
0 VALUE Ymin  \ в коорд системе windows

.E FVALUE МинАмплитуда         \ заполняется при анализе массива значений
.E FVALUE МаксАмплитуда 


: РасчетY  ( F: r1 -- r2 )
      МинАмплитуда F-    \ Ok
      Ymax Ymin - DS>F F* 
      МаксАмплитуда МинАмплитуда F- 
      F/                                                        
      Ymax DS>F FSWAP F- 
;

: Масштаб ( id -- ) \ *
     DUP ИскатьМакс FTO МаксАмплитуда 
         ИскатьМин FTO МинАмплитуда 
;
: ВремяТочки ( n -- F: r )
     DS>F ДлинаКванта F*
;

